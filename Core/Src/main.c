uint16_t filter_adc(uint16_t new_sample)
{
	sum -= adcBuffer[idx];         // Remove old sample
	adcBuffer[idx] = new_sample;   // Add new sample
	sum += new_sample;

	idx++;
	if (idx >= FILTER_DEPTH) idx = 0;

	return (uint16_t)(sum / FILTER_DEPTH);
}

void readControlChannels()
{
	uint16_t adcValues[2];

	HAL_ADC_Start(&hadc1);  // Start ADC

	for (int i = 0; i < 2; i++) {
		HAL_ADC_PollForConversion(&hadc1, HAL_MAX_DELAY);      // Wait for conversion
		adcValues[i] = HAL_ADC_GetValue(&hadc1);               // Read channel value
	}

	HAL_ADC_Stop(&hadc1);

	char ch1[16];
	char ch2[16];

	sprintf(ch1, "%d", (adcValues[0]<<4));
	sprintf(ch2, "%d", (adcValues[1]<<4));

	u8g2_ClearBuffer(&u8g2);
	u8g2_SetFont(&u8g2, u8g2_font_5x8_tr);
	u8g2_DrawStr(&u8g2, 0, 6, ch1);
	u8g2_DrawStr(&u8g2, 0, 15, ch2);
	u8g2_SendBuffer(&u8g2);
}

uint8_t u8x8_byte_hw_i2c_hal(u8x8_t *u8g2, uint8_t msg, uint8_t arg_int, void *arg_ptr)
{
	/* u8g2/u8x8 will never send more than 32 bytes between START_TRANSFER and END_TRANSFER */
	/* add extra byte for the i2c address */
	static uint8_t buffer[34];
	static uint8_t buf_idx;
	uint8_t *data;
	switch(msg)  {
	case U8X8_MSG_BYTE_SEND:
		data = (uint8_t *)arg_ptr;
		while( arg_int > 0 )
		{
			buffer[buf_idx++] = *data;
			data++;
			arg_int--;
		}
		break;
	case U8X8_MSG_BYTE_INIT:
		/* add your custom code to init i2c subsystem */
		break;
	case U8X8_MSG_BYTE_SET_DC:
		/* ignored for i2c */
		break;
	case U8X8_MSG_BYTE_START_TRANSFER:
		buf_idx = 0;
		break;
	case U8X8_MSG_BYTE_END_TRANSFER:
		HAL_I2C_Master_Transmit(&hi2c1, u8x8_GetI2CAddress(u8g2), (uint8_t *)buffer, buf_idx, HAL_MAX_DELAY);
		break;
	default:
		return 0;
	}
	return 1;
}

